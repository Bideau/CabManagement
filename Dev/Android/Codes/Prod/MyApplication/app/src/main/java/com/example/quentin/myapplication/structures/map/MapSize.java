package com.example.quentin.myapplication.structures.map;

public class MapSize {

	private int w;
	private int h;
	public MapSize() {
		w=0;
		h=0;
	}
	public int getW() {
		return w;
	}

	public void setW(int width) {
		this.w = width;
	}

	public int getH() {
		return h;
	}

	public void setH(int height) {
		this.h = height;
	}

}
